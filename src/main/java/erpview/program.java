    private void ConsultaProductos() throws IOException, SQLException {
        admintabla.removeAll();
        DefaultTableModel model = new DefaultTableModel() {
            @Override //Redefinimos el método getColumnClass
            public Class getColumnClass(int column){
                switch(column) {
                    case 1: return Object.class;
                    case 2: return Object.class;
                    case 3: return Object.class;
                    case 4: return Object.class;
                    case 5: return Object.class;
                    case 6: return ImageIcon.class;
                    default: return Object.class;
                }
            }
        };
        
        //DefaultTableModel model = new DefaultTableModel();
        llenarDatos listproduct = new llenarDatos();
        Object[] columnsName = new Object[6];
        
        columnsName[0] = "SKU";
        columnsName[1] = "Nombre";
        columnsName[2] = "Estado";
        columnsName[3] = "Categoria";
        columnsName[4] = "Stock";
        columnsName[5] = "Imagen";
        
        model.setColumnIdentifiers(columnsName);
        
        Object[] rowData = new Object[6];
        
        for(int i = 0; i < listproduct.LlenarTablaadminProd().size() ; i++){
            rowData[0] = listproduct.LlenarTablaadminProd().get(i).getSKU();
            rowData[1] = listproduct.LlenarTablaadminProd().get(i).getNombre();
            rowData[2] = listproduct.LlenarTablaadminProd().get(i).getEstado();
            rowData[3] = listproduct.LlenarTablaadminProd().get(i).getId();
            rowData[4] = listproduct.LlenarTablaadminProd().get(i).getStock();
            //rowData[5] = listproduct.LlenarTablaadminProd().get(i).getImagen();
            Blob blob = listproduct.LlenarTablaadminProd().get(i).getImagen();
            int blobLength = (int) blob.length();  
            byte[] bytes = blob.getBytes(1, blobLength);
            blob.free();
            BufferedImage img = ImageIO.read(new ByteArrayInputStream(bytes));
            ImageIcon icon = new ImageIcon(img); 
            rowData[5] = icon;
            model.addRow(rowData);
        }
        admintabla.setModel(model);
 }
