    private void adminPabrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminPabrirActionPerformed
        String ruta;
        String nombre;
        final JFileChooser elegirimg = new JFileChooser();
        elegirimg.setMultiSelectionEnabled(false);
        FileNameExtensionFilter filtroImagen = new FileNameExtensionFilter("JPG & GIF & BMP & PNG", "jpg", "gif", "bmp","png");
        elegirimg.setFileFilter(filtroImagen);
        //elegirimg.showOpenDialog(elegirimg);
        int o=elegirimg.showOpenDialog(elegirimg);
        if(o == JFileChooser.APPROVE_OPTION){ 
            ruta=elegirimg.getSelectedFile().getAbsolutePath();
            nombre=elegirimg.getSelectedFile().getName();
            Image preview = Toolkit.getDefaultToolkit().getImage(ruta);
            if(preview != null){
                ImageIcon imagen = new ImageIcon(preview.getScaledInstance(adminPimg.getWidth(), adminPimg.getHeight(), adminPimg.getWidth()));
                adminPimg.setIcon(imagen);
                adminPruta.setText(ruta);
            }
        }
        
    }//GEN-LAST:event_adminPabrirActionPerformed

    private void adminPagregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminPagregarActionPerformed
        buscadores id = new buscadores();
        Boolean guardar = true;
        String mensaje = "";
        String vsku = adminPsku.getText();
        String vnombre = adminPnombre.getText();
        int vstock = Integer.parseInt(adminPstock.getText());
        String vestado = adminPestado.getText();
        String vcategoria = adminPcategoria.getSelectedItem().toString();
        String vimagen = adminPruta.getText();
            if(vsku.length()==0){
                guardar = false;
                mensaje+="Escriba el sku, por favor\n";
            }
            if(vnombre.length()==0){
                guardar = false;
                mensaje+="Escriba el nombre, por favor\n";
            }
            if(vestado.length()==0){
                guardar = false;
                mensaje+="Escriba el estado, por favor\n";
            }
             if(vstock==0){
                guardar = false;
                mensaje+="Escriba la cantidad, por favor\n";
            }
            if(vimagen.length()==0){
                guardar = false;
                mensaje+="Debe elegir una imagen, por favor\n";
            } 
            if(guardar){
                controlDatos control = new controlDatos();
            try {
                System.out.println("Control de loa datos se enviaron correcto");
                control.insertarProducto(vsku,id.buscarIDcat(vcategoria),vnombre, vstock, vestado,vimagen);
            } catch (FileNotFoundException ex) {
                Logger.getLogger(program.class.getName()).log(Level.SEVERE, null, ex);
            }
                adminPsku.setText("");
                adminPnombre.setText("");
                adminPestado.setText("");
                adminPimg.setText("");
                adminPcategoria.setSelectedIndex(0);
            try {
                ConsultaProductos();
            } catch (IOException ex) {
                Logger.getLogger(program.class.getName()).log(Level.SEVERE, null, ex);
            } catch (SQLException ex) {
                Logger.getLogger(program.class.getName()).log(Level.SEVERE, null, ex);
            }
            }
            else JOptionPane.showMessageDialog(null, mensaje);
    }                                          

                                  

